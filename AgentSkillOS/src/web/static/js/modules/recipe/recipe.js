/**
 * Recipe module slice.
 * Provides recipe save, recommend, and execute functionality.
 */
function recipeSlice() {
    return {
        // Trigger recipe recommendation (called in parallel with skill search)
        triggerRecipeRecommendation() {
            if (!this.taskInput.trim() || this.recipes.length === 0) return;
            this.recipeSearching = true;
            this.ws.send(JSON.stringify({
                type: 'recommend_recipes',
                prompt: this.taskInput
            }));
        },

        // Execute a recipe directly (skip search)
        executeRecipe(recipeId) {
            const files = this.uploadedFiles.map(f => f.path);
            this.ws.send(JSON.stringify({
                type: 'execute_recipe',
                recipe_id: recipeId,
                files: files
            }));
        },

        // Save current execution as a recipe (description auto-generated by LLM)
        saveRecipe() {
            if (!this.saveRecipeName.trim()) return;
            this.savingRecipe = true;
            this.ws.send(JSON.stringify({
                type: 'save_recipe',
                name: this.saveRecipeName
            }));
        },

        // Fetch all recipes from backend
        fetchRecipes() {
            this.ws.send(JSON.stringify({ type: 'get_recipes' }));
        },

        // Delete a recipe
        deleteRecipe(recipeId) {
            this.ws.send(JSON.stringify({
                type: 'delete_recipe',
                recipe_id: recipeId
            }));
        },

        // Open save recipe modal
        openSaveRecipeModal() {
            this.showSaveRecipeModal = true;
            this.saveRecipeName = '';
        },

        // Close save recipe modal
        closeSaveRecipeModal() {
            this.showSaveRecipeModal = false;
            this.savingRecipe = false;
        },

        // Open recipe detail modal
        openRecipeDetail(recipe) {
            this.selectedRecipeDetail = recipe;
        },

        // Close recipe detail modal
        closeRecipeDetail() {
            this.selectedRecipeDetail = null;
        }
    };
}

window.recipeSlice = recipeSlice;
