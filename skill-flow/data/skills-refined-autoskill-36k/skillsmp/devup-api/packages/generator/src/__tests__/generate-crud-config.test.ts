import { describe, expect, test } from 'bun:test'
import type { OpenAPIV3_1 } from 'openapi-types'
import {
  generateCrudConfigCode,
  generateCrudConfigTypes,
} from '../generate-crud-config'

// =============================================================================
// Helper
// =============================================================================

const createDocument = (
  doc: Partial<OpenAPIV3_1.Document> = {},
): OpenAPIV3_1.Document =>
  ({
    openapi: '3.1.0',
    info: { title: 'Test API', version: '1.0.0' },
    paths: {},
    ...doc,
  }) as OpenAPIV3_1.Document

const createCrudDocument = (
  name: string,
  fields: Record<string, OpenAPIV3_1.SchemaObject> = {},
  required: string[] = [],
): OpenAPIV3_1.Document =>
  createDocument({
    paths: {
      [`/${name}s/{id}`]: {
        get: {
          operationId: `get${name.charAt(0).toUpperCase()}${name.slice(1)}`,
          tags: [`devup:${name}:one`],
          responses: { '200': { description: 'Success' } },
        },
        put: {
          operationId: `update${name.charAt(0).toUpperCase()}${name.slice(1)}`,
          tags: [`devup:${name}:edit`],
          requestBody: {
            content: {
              'application/json': {
                schema: {
                  type: 'object',
                  properties: fields,
                  required,
                },
              },
            },
          },
          responses: { '200': { description: 'Success' } },
        },
        patch: {
          operationId: `patch${name.charAt(0).toUpperCase()}${name.slice(1)}`,
          tags: [`devup:${name}:fix`],
          requestBody: {
            content: {
              'application/json': {
                schema: {
                  type: 'object',
                  properties: fields,
                },
              },
            },
          },
          responses: { '200': { description: 'Success' } },
        },
      },
      [`/${name}s`]: {
        post: {
          operationId: `create${name.charAt(0).toUpperCase()}${name.slice(1)}`,
          tags: [`devup:${name}:create`],
          requestBody: {
            content: {
              'application/json': {
                schema: {
                  type: 'object',
                  properties: fields,
                  required,
                },
              },
            },
          },
          responses: { '201': { description: 'Created' } },
        },
      },
    },
  })

