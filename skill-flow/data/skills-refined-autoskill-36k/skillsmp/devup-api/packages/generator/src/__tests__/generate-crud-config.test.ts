describe('generateCrudConfigCode', () => {
  test('generates header comments and imports', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain(
      '// Auto-generated CRUD components from OpenAPI specs',
    )
    expect(result).toContain('// Do not edit this file directly')
    expect(result).toContain("'use client';")
    expect(result).toContain(
      "import { ApiForm, useFormContext } from '@devup-api/hookform';",
    )
  })

  test('generates component for CRUD group', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('// UserCrud')
    expect(result).toContain('function UserCrudFields()')
    expect(result).toContain('export function UserCrud(')
  })

  test('generates default export object', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('export default {')
    expect(result).toContain('user: UserCrud,')
  })

  test('handles multiple CRUD groups', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createDocument({
        paths: {
          '/users/{id}': {
            get: {
              operationId: 'getUser',
              tags: ['devup:user:one'],
              responses: { '200': { description: 'Success' } },
            },
          },
          '/users': {
            post: {
              operationId: 'createUser',
              tags: ['devup:user:create'],
              responses: { '201': { description: 'Created' } },
            },
          },
          '/posts/{id}': {
            get: {
              operationId: 'getPost',
              tags: ['devup:post:one'],
              responses: { '200': { description: 'Success' } },
            },
          },
          '/posts': {
            post: {
              operationId: 'createPost',
              tags: ['devup:post:create'],
              responses: { '201': { description: 'Created' } },
            },
          },
        },
      }),
    })

    expect(result).toContain('UserCrud')
    expect(result).toContain('PostCrud')
    expect(result).toContain('user: UserCrud,')
    expect(result).toContain('post: PostCrud,')
  })

  test('handles empty schemas', () => {
    const result = generateCrudConfigCode({})

    expect(result).toContain("'use client';")
    expect(result).toContain('export default {')
  })

  test('handles documents without valid CRUD configs', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createDocument({
        paths: {
          '/users': {
            get: {
              operationId: 'listUsers',
              tags: ['users'],
              responses: { '200': { description: 'Success' } },
            },
          },
        },
      }),
    })

    expect(result).toContain("'use client';")
    expect(result).toContain('export default {')
    expect(result).not.toContain('function')
  })
})

