describe('generateCrudConfigCode - component structure', () => {
  test('generates useFormContext hook usage', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain(
      'const { register, formState: { errors } } = useFormContext();',
    )
  })

  test('generates error display for fields', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }, [
        'name',
      ]),
    })

    expect(result).toContain('{errors.name && <span>Required</span>}')
  })

  test('generates Invalid text for optional fields', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', {
        nickname: { type: 'string' },
      }),
    })

    expect(result).toContain('{errors.nickname && <span>Invalid</span>}')
  })

  test('generates submit button', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('<button type="submit">Submit</button>')
  })

  test('generates ApiForm with correct props', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('<ApiForm')
    expect(result).toContain('api={apiClient}')
    expect(result).toContain("method={isEdit ? 'put' : 'post'}")
    expect(result).toContain('onSuccess={onSuccess}')
    expect(result).toContain('onError={onError}')
  })

  test('generates fetchDefaultValues for edit mode', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('fetchDefaultValues={isEdit ? {')
    expect(result).toContain("path: 'getUser',")
    expect(result).toContain('options: { params },')
  })

  test('generates isEdit check', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain('const isEdit = !!params;')
  })

  test('uses edit operationId when available', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createCrudDocument('user', { name: { type: 'string' } }),
    })

    expect(result).toContain("path={isEdit ? 'updateUser' : 'createUser'}")
  })

  test('uses fix operationId when edit not available', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createDocument({
        paths: {
          '/users/{id}': {
            get: {
              operationId: 'getUser',
              tags: ['devup:user:one'],
              responses: { '200': { description: 'Success' } },
            },
            patch: {
              operationId: 'patchUser',
              tags: ['devup:user:fix'],
              responses: { '200': { description: 'Success' } },
            },
          },
          '/users': {
            post: {
              operationId: 'createUser',
              tags: ['devup:user:create'],
              responses: { '201': { description: 'Created' } },
            },
          },
        },
      }),
    })

    expect(result).toContain("path={isEdit ? 'patchUser' : 'createUser'}")
    expect(result).toContain("method={isEdit ? 'patch' : 'post'}")
  })

  test('generates no fields comment when schema has no properties', () => {
    const result = generateCrudConfigCode({
      'openapi.json': createDocument({
        paths: {
          '/users/{id}': {
            get: {
              operationId: 'getUser',
              tags: ['devup:user:one'],
              responses: { '200': { description: 'Success' } },
            },
          },
          '/users': {
            post: {
              operationId: 'createUser',
              tags: ['devup:user:create'],
              responses: { '201': { description: 'Created' } },
            },
          },
        },
      }),
    })

    expect(result).toContain('{/* No fields defined in OpenAPI schema */}')
  })
})

